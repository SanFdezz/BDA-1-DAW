use biblioteca;

DROP FUNCTION IF EXISTS SUMA2ENTEROSA;
DELIMITER $$
	CREATE FUNCTION SUMA2ENTEROSA(
    )
    RETURNS INT
    DETERMINISTIC
    BEGIN
	RETURN 2 + 5;
    END $$
    
DELIMITER ;

SELECT SUMA2ENTEROSA() AS SUMA;

DROP FUNCTION IF EXISTS SUMA2ENTEROSB;
DELIMITER $$
	CREATE FUNCTION SUMA2ENTEROSB(
		p_A INT,
		p_B INT
    )
    RETURNS INT
    DETERMINISTIC
    BEGIN
	RETURN p_A + p_B;
    END $$
    
DELIMITER ;

SELECT SUMA2ENTEROSB(5,5) AS SUMA2;

DROP FUNCTION IF EXISTS SUMA2ENTEROSC;
DELIMITER $$
	CREATE FUNCTION SUMA2ENTEROSC(
		p_A INT,
		p_B INT
    )
    RETURNS INT
    DETERMINISTIC
    BEGIN
		DECLARE RESULTADO INT;
        SET RESULTADO = p_A + p_B;
	RETURN RESULTADO;
    END $$
    
DELIMITER ;

SELECT SUMA2ENTEROSC(1,2) AS SUMA3;






